    private void carregarTabela() throws SQLException {
        ProntuarioBO prontuarioBO = new ProntuarioBO();
        this.listaProntuario = prontuarioBO.buscarTodos();
        ModeloTabela model = new ModeloTabela();
        this.tblProntuario.setModel(model);
    }
    
    private class ModeloTabela extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return listaProntuario.size();
        }
        
        @Override
        public int getColumnCount() {
            return 3;
        }

        
        public Object getValueAt(int linha, int coluna) {
            Prontuario prontuario = listaProntuario.get(linha);
            if (coluna == 0) {
                 return prontuario.getConsulta().getUsuario().getNome();
              
            } else if (coluna == 1) {
                return prontuario.getConsulta().getPaciente().getNome();
                //return consulta.getHorario();
            } else if (coluna == 2) {
                return prontuario.getProntuario();
                //return consulta.getPaciente().getNome();
            }
            return null;
        }
    }
